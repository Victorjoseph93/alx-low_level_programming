#include <stdio.h>
/**
 * add - function that add to numbers
 * @a: first int
 * @b: second int
 * Return: addition result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * div - function that add to numbers
 * @a: first int
 * @b: second int
 * Return: division result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - function that find mod of two numbers
 * @a: first int
 * @b: second int
 * Return: first  result
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul - function that multiply to numbers
 * @a: first int
 * @b: second int
 * Return: multiply result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * sub - function that subtract two numbers
 * @a: first int
 * @b: second int
 * Return: subtraction result
 */
int sub(int a, int b)
{
	return (a - b);
}
